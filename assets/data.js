function mergeWordsToSentences(sentences, words) {
    for (let key in words) {
        let sentence = sentences[key];
        sentence.words = words[key].words;
        sentence.score = words[key].score;
    }
}

function mergeData (data) {
    return Object.values(data).reduce((total, amount) => {
        const sentence = amount.words.reduce((t, m) => {
            t.push(m.word)
            return t
        }, [])
        total.push(sentence)
        return total
    }, [])
}

